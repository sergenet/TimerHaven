// Current language (default to English or browser language if available)
let currentLanguage = 'en';

// Initialize i18n
function initI18n() {
    // Try to get language from localStorage or browser
    const savedLang = localStorage.getItem('timerhaven-language');
    const browserLang = navigator.language.split('-')[0];
    
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    } else if (translations[browserLang]) {
        currentLanguage = browserLang;
    }
    
    // Apply translations
    applyTranslations();
}

// Get translation for a key
function t(key) {
    return translations[currentLanguage]?.[key] || translations.en[key] || key;
}

// Switch language
function switchLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('timerhaven-language', lang);
        applyTranslations();
        
        // Update HTML lang attribute
        document.documentElement.lang = lang;
        
        // Update HTML dir attribute for RTL languages
        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
        }
        
        // Re-initialize hero clock with new language
        if (typeof updateHeroClock === 'function') {
            updateHeroClock();
        }
        
        // Re-initialize cookie consent
        if (typeof initCookieConsent === 'function') {
            initCookieConsent();
        }
    }
}

// Apply translations to the page
function applyTranslations() {
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        // Check if we should translate a specific attribute
        const attr = element.getAttribute('data-i18n-attr');
        if (attr) {
            element.setAttribute(attr, translation);
        } else {
            element.textContent = translation;
        }
    });
    
    // Update the language selector
    const langSelector = document.getElementById('language-selector');
    if (langSelector) {
        langSelector.value = currentLanguage;
    }
}

